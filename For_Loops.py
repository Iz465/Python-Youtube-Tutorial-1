
for item in 'Python': # this for loop will activate six times as python is 6 characters long
  print(item) # this will print each letter each line

for item in ['Mosh', 'John', 'Sarah']: #this will print each name per line
  print(item) 

for item in [1,2,3,4]: # same result as above
  print(item)

for item in range(10): #T his will print the range of numbers from 0 to 9. 10 wont be included.
  print(item)

for item in range(5,10): # Will print 5,6,7,8,9
  print(item)

for item in range(5,10,2): # the third number is a step. This step is 2, meaning it will skip 2 each time
  print(item) # this means it will come out as 5,7,9