import math # I imported all the math methods and can now use them

print(math.ceil(2.1)) # using the ceil method to get the ceiling of a number, only available via using math function. 
# ceiling method will give 3, it gives the whole number at its highest, wont round it. so, 2.1 wouldnt be 2 but 3 with ceil

x = 2.9
print(round(x)) # Rounds it to the nearest whole number. Will print 2.9 as 3

print(abs(-2.9)) # abs always gives a positive number. -2.9 will be printed out as 2.9

